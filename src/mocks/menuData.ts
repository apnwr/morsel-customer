// Helper functions
export function getMenuForRestaurant(restaurantId: string) {
  return menuData[restaurantId] || { categories: [], items: [] };
}

export function getMenuItemById(
  restaurantId: string,
  itemId: string
): MenuItem | undefined {
  const menu = menuData[restaurantId];
  return menu?.items.find((item) => item.id === itemId);
}

export function getItemsByCategory(
  restaurantId: string,
  categoryId: string
): MenuItem[] {
  const menu = menuData[restaurantId];
  return menu?.items.filter((item) => item.categoryId === categoryId) || [];
}
